COLORMAP = np.array(_PALETTE, dtype=np.uint8)


def mmh_to_dbz(R: np.ndarray) -> np.ndarray:
    """Marshall-Palmer reflectivity. Matches dgmr-rs snapshot.rs:11."""
    R = np.asarray(R, dtype=np.float32)
    out = np.zeros_like(R)
    mask = R > 1e-4
    out[mask] = 10.0 * np.log10(200.0 * np.power(R[mask], 1.6))
    return out


def dbz_to_index(dbz: np.ndarray) -> np.ndarray:
    """dBZ → palette index in [0, 255]. Matches dgmr-rs snapshot.rs:19."""
    return np.clip(dbz * 2.0, 0.0, 255.0).astype(np.uint8)


def precip_to_rgb(R_mmhr: np.ndarray) -> np.ndarray:
    """mm/hr field (H, W) → (H, W, 3) uint8 RGB via the dgmr-rs palette."""
    return COLORMAP[dbz_to_index(mmh_to_dbz(R_mmhr))]
